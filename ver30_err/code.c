void mytolower(char *str) {
  while (*str) {
    if (*str >= 'A') {
      *str = 'a' + *str - 'A';
    }
    str++;
  }
}

void mytoupper(char *str) {
  int len = strlen(str);
  for (int i = 0; i < len + 1; i++) {
    *(str + i) = toupper(*(str + i));
  }
}

void mytoupper2(char *str) {
  char *ptr = str;
  while (*ptr != '\0') {
    if (*ptr >= 'a' && *ptr <= 'z') *ptr = *ptr - ('a' - 'A');
    ptr++;
  }
}

